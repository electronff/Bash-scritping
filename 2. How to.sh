Example: Hello World

1: Create the script: # touch hello.sh

2: Give execute permission to the file: # chmod +x hello.sh

3: Get into the file: # vim hello.sh 

4: Insert the commands to display Hello World and save the file:

￭ echo “Hello World” then save and quit the vim (:wq)

5: Run the script: ./hello.sh